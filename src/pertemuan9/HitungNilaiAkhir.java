public class HitungNilaiAkhir {
    public double hitung(double nilai10, double nilai15_1, double nilaiUTS, double nilai15_2, double nilai35) {
        return (nilai10 * 0.10) + (nilai15_1 * 0.15) + (nilaiUTS * 0.25) + (nilai15_2 * 0.15) + (nilai35 * 0.35);
    }
}


